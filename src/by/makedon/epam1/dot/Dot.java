package by.makedon.epam1.dot;

public class Dot {
    private double x;
    private double y;

    public Dot() {

    }

    public Dot(double x, double y) {
        this.x = x;
        this.y = y;
    }

    public void setX(double x) {
        this.x = x;
    }

    public void setY(double y) {
        this.y = y;
    }

    public double getX() {
        return x;
    }

    public double getY() {
        return y;
    }
}
